    public void retData(){
        DefaultTableModel dm=new DefaultTableModel();
        dm.addColumn("Roll No.");
        dm.addColumn("Name");
        dm.addColumn("Year");
        dm.addColumn("Branch");
        dm.addColumn("Group Formed");
        
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/projob","root","root");                
		String sql = "select * from student";
                // execute insert SQL stetement
                Statement s=con.prepareStatement(sql);
                ResultSet rs=s.executeQuery(sql);
                while(rs.next()){
                    String roll=rs.getString(1);
                    String name=rs.getString(2);
                    String year=rs.getString(3);
                    String branch=rs.getString(4);
                    String groupformed=rs.getString(5);
                    
                    System.out.println(roll+"\t"+name+"\t"+year+"\t"+branch+"\t"+groupformed);
                    dm.addRow(new String[]{roll,name,year,branch,groupformed});
                }

                jTable1.setModel(dm);
		//System.out.println("Record is inserted into DBUSER table!");        
        }
        catch(ClassNotFoundException | SQLException e){}
    }

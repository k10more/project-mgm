    public Boolean updateData(String roll, String name,String year,String branch,String groupformed){
        String sql="update student set name='"+name+"', year='"+year+"', branch='"+branch+"', groupformed='"+groupformed+"' where rollno="+roll;
        try{ 
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/projob","root","root");
            
            Statement s=con.prepareStatement(sql);
            s.execute(sql);
            return true;
        }
        catch(ClassNotFoundException | SQLException e){
            return false;
        }
    }
    
    
